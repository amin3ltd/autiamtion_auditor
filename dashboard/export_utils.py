def save_export(formats: List[str], output_dir: str, report, evidences, opinions) -> Dict[str, str]:
    """
    Save exports in multiple formats.
    
    Args:
        formats: List of formats ('markdown', 'text', 'json', 'html', 'video')
        output_dir: Directory to save files
        report: AuditReport object
        evidences: Dictionary of evidence
        opinions: List of opinions
    
    Returns:
        Dict mapping format to file path
    """
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    repo_name = report.repo_url.split('/')[-1].replace('.git', '')
    
    saved = {}
    
    if 'markdown' in formats:
        md_file = output_path / f"audit_report_{repo_name}_{timestamp}.md"
        md_file.write_text(export_to_markdown(report, evidences, opinions), encoding='utf-8')
        saved['markdown'] = str(md_file)
    
    if 'text' in formats:
        txt_file = output_path / f"audit_report_{repo_name}_{timestamp}.txt"
        txt_file.write_text(export_to_text(report, evidences, opinions), encoding='utf-8')
        saved['text'] = str(txt_file)
    
    if 'json' in formats:
        json_file = output_path / f"audit_report_{repo_name}_{timestamp}.json"
        json_file.write_text(export_to_json(report, evidences, opinions), encoding='utf-8')
        saved['json'] = str(json_file)
    
    if 'html' in formats:
        html_file = output_path / f"audit_report_{repo_name}_{timestamp}.html"
        html_file.write_text(export_to_html(report, evidences, opinions), encoding='utf-8')
        saved['html'] = str(html_file)
    
    if 'video' in formats:
        video_file = output_path / f"video_script_{repo_name}_{timestamp}.txt"
        video_file.write_text(generate_video_script(report, evidences, opinions), encoding='utf-8')
        saved['video'] = str(video_file)
    
    return saved
